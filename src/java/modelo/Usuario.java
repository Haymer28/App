package modelo;

/**
 *
 * @author SAMSUNG-PC
 */
public class Usuario {
    private int id;
    private String nombre;
    private String celular;
    private String email;
    private String pass;
    private String rol;

    public Usuario() {
    }

    public Usuario(int id, String nombre, String celular, String email, String pass, String rol) {
        this.id = id;
        this.nombre = nombre;
        this.celular = celular;
        this.email = email;
        this.pass = pass;
        this.rol = rol;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    
    
    
}
